import React from 'react';
import Icon, {TYPE, ICON_COLOR} from './Icon';
import {shallow} from 'enzyme';

test('render if type', () => {
  const icon = shallow(
    <Icon type={TYPE.ANSWER} />
  );

  expect(icon.hasClass('sg-icon')).toEqual(true);
  expect(icon.find('use')).toHaveLength(1);
});

test('render if children', () => {
  const icon = shallow(
    <Icon>
      <div>
        <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24">
          <path fillRule="nonzero" d="M8.45 1v4.84h3.57L6.5 18.74H2v4.85h12.9v-4.84h-3.56l5.52-12.9h4.5V1z" />
        </svg>
      </div>
    </Icon>
  );

  expect(icon.hasClass('sg-icon')).toEqual(true);
  expect(icon.find('svg')).toHaveLength(1);
});

test('error when no type and no children', () => {
  const spy = jest.spyOn(console, 'error');

  console.error = jest.fn();
  shallow(
    <Icon />);
  expect(console.error.mock.calls).toHaveLength(1);

  spy.mockRestore();
});

test('error when type and children', () => {
  const spy = jest.spyOn(console, 'error');

  console.error = jest.fn();
  shallow(
    <Icon type={TYPE.ANSWER}>
      <div>
        <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24">
          <path fillRule="nonzero" d="M8.45 1v4.84h3.57L6.5 18.74H2v4.85h12.9v-4.84h-3.56l5.52-12.9h4.5V1z" />
        </svg>
      </div>
    </Icon>);
  expect(console.error.mock.calls).toHaveLength(1);

  spy.mockRestore();
});

test('type passed to xlink:href', () => {
  const type = TYPE.ANSWER;
  const icon = shallow(
    <Icon type={type} />
  );
  const use = icon.find('use');

  expect(use.props().xlinkHref).toEqual('#icon-' + type);
});

test('colors', () => {
  const type = TYPE.ANSWER;
  const color = ICON_COLOR.DARK;
  const icon = shallow(
    <Icon type={type} color={color} />
  );

  expect(icon.hasClass(`sg-icon--${color}`)).toEqual(true);
});

test('size', () => {
  const size = 10;
  const type = TYPE.ANSWER;
  const icon = shallow(
    <Icon type={type} size={size} />
  );

  expect(icon.hasClass(`sg-icon--x${size}`)).toEqual(true);
});

test('other props', () => {
  const type = TYPE.ANSWER;
  const icon = shallow(
    <Icon type={type} data-something={'else'} />
  );

  expect(icon.find('[data-something="else"]')).toHaveLength(1);
});
